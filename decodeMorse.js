function decodeMorse(morseCode) {
  words = morseCode.split('   ');
  res = [];
  words.forEach(word => {
    chars = word.split(' ');
    var dWord = [];
    chars.forEach(char => {
      dWord.push(MORSE_CODE[char]);
    });
    res.push(dWord.join(''));
  });

  return res.join(' ').trim();
}

